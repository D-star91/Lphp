                        <h6>၆</h6> 	ထို​အ​ကြောင်း​ကြောင့် လူ​တို့​ရှေ့၌ ကိုယ်​ခ​န္ဓာ​အား​ဖြင့် အ​ပြစ်​စီ​ရင်​ခြင်း​ကို ခံ​ရ​သော်​လည်း၊ ဘု​ရား​သ​ခင့်​ရှေ့​တော်၌ စိတ်​ဝိ​ညာဉ်​အား​ဖြင့် အ​သက်​ရှင်​မည်​အ​ကြောင်း၊ သေ​သော​သူ​တို့​သည် ဧ​ဝံ​ဂေ​လိ​တ​ရား​ကို ကြား​ရ​ကြ၏။<br>
                        
                        <h6>၇</h6> 	ခပ်​သိမ်း​သော​အ​မှု​အ​ရာ​တို့​သည် အ​ဆုံး​သို့​ရောက်​လု​သည် ဖြစ်​သော​ကြောင့်၊ သ​မ္မာ​သ​တိ​ရှိ​ကြ​လော့။ ဆု​တောင်း​ခြင်း​အ​လို​ငှာ စောင့်​နေ​ကြ​လော့။-<br>
                        <h6>၈</h6> 	အ​ခြား​သော​အ​မှု​အ​ရာ​ထက်​မ​က၊ အ​ချင်း​ချင်း​အား​ကြီး​သော​ချစ်​ခြင်း​မေ​တ္တာ​ရှိ​ကြ​လော့။ ချစ်​ခြင်း​မေ​တ္တာ​သည် များ​စွာ​သော အ​ပြစ်​တို့​ကို ဖုံး​အုပ်​တတ်၏။-<br>
                        <h6>၉</h6> 	မြည်​တမ်း​ခြင်း​မ​ရှိ​ဘဲ အ​ချင်း​ချင်း ဧည့်​သည်​ဝတ်​ကို ပြု​ကြ​လော့။-<br>
                        <h6>၁၀</h6> သင်​တို့​သည် ဆု​ကျေး​ဇူး​တော်​ကို အ​သီး​အ​သီး​ခံ​ရ​ကြ​သည်​နှင့် အ​ညီ၊ ဘု​ရား​သ​ခင်၏ အ​ထူး​ထူး အ​ပြား​ပြား​သော ကျေး​ဇူး​တော်​ဘ​ဏ္ဍာ​စိုး​ကောင်း​ကဲ့​သို့၊ အ​ချင်း​ချင်း ဝေ​ငှ​ပေး​ကမ်း​ကြ​လော့။-<br>
                        <h6>၁၁</h6>	ဘု​ရား​သ​ခင်​သည် အ​ရာ​ရာ၌ ယေ​ရှု​ခ​ရစ်​အား​ဖြင့် ဘုန်း​တော်​ထင်​ရှား​စေ​မည်​အ​ကြောင်း၊ ဟော​ပြော​သော​သူ​သည် ဘု​ရား​သ​ခင်၏ ဗျာ​ဒိတ်​တော်​နှင့်​အ​ညီ ဟော​ပြော​စေ။ အ​ခြား​သော​အ​မှု​ကို ဆောင်​ရွက်​သော​သူ​သည်၊ ဘု​ရား​သ​ခင်​ပေး​သ​နား​တော်​မူ​သော အ​စွမ်း​သ​တ္တိ​အ​တိုင်း ဆောင်​ရွက်​စေ။ ထို​ဘု​ရား​သ​ခင်​သည် က​မ္ဘာ​အ​ဆက်​ဆက် ဘုန်း​အ​သ​ရေ အ​စွမ်း​သ​တ္တိ ရှိ​တော်​မူ​စေ​သ​တည်း။ အာ​မင်။<br>
                        
                        <h6>၁၂</h6>	ချစ်​သူ​တို့၊ သင်​တို့​ကို စုံ​စမ်း​စ​ရာ​ဖို့ ယ​ခု​ခံ​ရ​သော မီး​စစ်​ခြင်း​အ​ရာ​ကို​ကား၊ သင်​တို့၌ အံ့​ဩ​ဖွယ်​သော​အ​ရာ ဖြစ်​သည်​ဟု ထင်​မှတ်၍၊ အံ့​ဩ​ခြင်း မ​ရှိ​ကြ​နှင့်။-<br>
                        <h6>၁၃</h6>	သင်​တို့​သည် ခ​ရစ်​တော်၏​ဆင်း​ရဲ​ကို ဆက်​ဆံ​ကြ​သည်​ဖြစ်၍၊ ဘုန်း​တော်​ပေါ်​ထွန်း​သော​အ​ခါ အ​လွန်​ဝမ်း​မြောက်​ရွှင်​လန်း​မည်​အ​ကြောင်း၊ ယ​ခု​ပင် ဝမ်း​မြောက်​ကြ​လော့။- <br>
                        <h6>၁၄</h6>	ခ​ရစ်​တော်၏ နာ​မ​ကြောင့် ကဲ့​ရဲ့​ခြင်း​ကို​ခံ​ရ​လျှင် မင်္ဂ​လာ​ရှိ​ကြ၏။ အ​ကြောင်း​မူ​ကား၊ ဘု​ရား​သ​ခင်၏ ဘုန်း​ကြီး​သော ဝိ​ညာဉ်​တော်​သည် သင်​တို့​အ​ပေါ်​မှာ ကျိန်း​ဝပ်​တော်​မူ၏။ သူ​တို့၌​ကား ကဲ့​ရဲ့​ခြင်း၊ သင်​တို့၌​ကား ချီး​မွမ်း​ခြင်း​ရှိ​တော်​မူ၏။-<br>
                        <h6>၁၅</h6> လူ​အ​သက်​ကို​သတ်​သော အ​ပြစ်၊ သူ့​ဥ​စ္စာ​ကို​ခိုး​သော အ​ပြစ်၊ ဒု​စ​ရိုက်​ကို​ပြု​သော အ​ပြစ်၊ သူ​တစ်​ပါး​အ​မှု၌ ရော​နှော​သော​အ​ပြစ် ရှိ၍၊ သင်​တို့​တွင် အ​ဘယ်​သူ​မျှ မ​ခံ​စေ​နှင့်။- <br>
                        <h6>၁၆</h6> ခ​ရစ်​ယာန်​ဖြစ်​သော​ကြောင့် ခံ​ရ​လျှင်​မူ​ကား၊ ရှက်​ကြောက်​ခြင်း မ​ရှိ​စေ​နှင့်။ ထို​အ​မှု​ကြောင့်​ခံ​ရ​လျှင် ဘု​ရား​သ​ခင်၏ ဂုဏ်​တော်​ကို​ချီး​မွမ်း​စေ။-<br>
                        <h6>၁၇</h6> ဘု​ရား​သ​ခင်၏​အိမ်​တော်​သား​တို့​ကို ရှေ့​ဦး​စွာ စစ်​ကြော​စီ​ရင်​ရ​သော အ​ချိန်​ရှိ​ပြီ။ ငါ​တို့​ကို ရှေ့​ဦး​စွာ စစ်​ကြော​စီ​ရင်​လျှင်၊ ဘု​ရား​သ​ခင်၏ ဧ​ဝံ​ဂ​လိ​တ​ရား​စ​ကား​ကို နား​မ​ထောင်​သော​သူ​တို့၏ အ​မှု​သည် အ​ဘယ်​သို့ ပြီး​လိမ့်​မည်​နည်း။-<br>
                        <h6>၁၈</h6>	ဖြောင့်​မတ်​သော​သူ​သည် ကယ်​တင်​ခြင်း​သို့ ရောက်​နိုင်​ခဲ​သည်​မှန်​လျှင်၊ ဘု​ရား​သ​ခင်​ကို မ​ကိုး​ကွယ်​သော​သူ၊ အ​ပြစ်​ထင်​ရှား​သော​သူ​သည် အ​ဘယ်​မှာ ပေါ်​လိမ့်​မည်​နည်း။-<br>
