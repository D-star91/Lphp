                        <h6>၁</h6> 	ထို​သို့​ခ​ရစ်​တော်​သည် ကိုယ်​ခ​န္ဓာ​တော်၌ ငါ​တို့​အ​တွက် ခံ​တော်​မူ​သည်​မှန်​သော​ကြောင့်၊ သင်​တို့​လည်း ထို​သို့​သော​သ​ဘော​ကို လက်​နက်​ပြု၍ သုံး​စွဲ​ကြ​လော့။ အ​ကြောင်း​မူ​ကား၊ ကိုယ်​ခ​န္ဓာ၌​ခံ​သော​သူ​သည် ဒု​စ​ရိုက်​တို့​နှင့် လွတ်​ငြိမ်း​သည်​ဖြစ်၍၊-<br>
                        <h6>၂</h6> 	နောက်​တစ်​ဖန် သူ​တို့၏ တပ်​မက်​ခြင်း​တ​ရား​ကို မ​ကျင့်၊ ဘု​ရား​သ​ခင်၏ အ​လို​တော်​အ​တိုင်း​ကျင့်​လျက်၊ ကိုယ်​ခ​န္ဓာ​အ​သက်​အ​ပိုင်း​အ​ခြား ကျန်​ရစ်​သော​ကာ​လ​ကို လွန်​စေ​တတ်၏။-<br>
                        <h6>၃</h6>	သာ​သ​နာ​ပ​လူ​တို့၏ အ​လို​သို့​လိုက်၍၊ ကိ​လေ​သာ​ညစ်​ညူး​ခြင်း၊ ကာ​မ​တပ်​မက်​ခြင်း၊ စ​ပျစ်​ရည်​နှင့် ယစ်​မူး​ခြင်း၊ ပွဲ​လုပ်​ခြင်း၊ သောက်​ကြူး​ခြင်း၊ အ​ဓ​မ္မ​ရုပ်​တု​ကို ကိုး​ကွယ်​ခြင်း၌ ကျင်​လည်၍၊ ငါ​တို့​လွန်​စေ​ပြီး​သော အ​သက်​ကာ​လ​သည် ငါ​တို့​အား လောက်​ပေ၏။-<br>
                        <h6>၄ </h6>	ယ​ခု​မှာ သင်​တို့​သည် သူ​တို့​နှင့်​အ​တူ မ​ဆက်​ဆံ၊ ကာ​မ​ဂုဏ်၌ လွန်​ကျူး​ညစ်​ညူး​စွာ မ​ပြု​သည်​အ​ရာ​ကို သူ​တို့​သည်​အံ့​ဩ၍ ကဲ့​ရဲ့​တတ်​ကြ၏။-<br>
